CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID NUMBER,
    CONSTRAINT EMPLOYEE_PK PRIMARY KEY (EMPLOYEE_ID),
    LAST_NAME NVARCHAR2(50),
    FIRST_NAME NVARCHAR2(50),
    TITLE NVARCHAR2(100),
    TITLE_OF_COURTESY NVARCHAR2(100),
    BIRTH_DATE TIMESTAMP,
    HIRE_DATE TIMESTAMP,
    ADDRESS NVARCHAR2(100),
    CITY NVARCHAR2(50),
    REGION NVARCHAR2(50),
    POSTAL_CODE NUMBER,
    COUNTRY NVARCHAR2(50),
    HOME_PHONE NUMBER,
    EXTENSION TIMESTAMP,
    PHOTO NVARCHAR2(50),
    NOTES CLOB,
    REPORTS_TO NVARCHAR2(100)
);